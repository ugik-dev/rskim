<script>
$(document).ready(function() {
  $('#dashboard').addClass('active');

  var dataInfo = {};
  getInformasi();
  function getInformasi(){
    return $.ajax({
      url: `<?php echo site_url('SharedController/getInformasi/')?>`, 'type': 'POST',
      data: {},
      success: function (data){
        var json = JSON.parse(data);
        if(json['error']){
          return;
        }
        dataInfo = json['data'];
        renderInfo(dataInfo);
      },
      error: function(e) {}
    });
  }

  function renderInfo(dataInfo){
  var hitung1 = Math.round((dataInfo['kriteria1ok']/20436)*100);
  var hitung2 = Math.round((dataInfo['kriteria2ok']/23131)*100);
  var hitung3 = Math.round((dataInfo['kriteria3ok']/37007)*100);
 
  $('#data_j1').html(` : `+convertToNum(dataInfo['kriteria1']));
  $('#data_j1_ok').html(` : `+convertToNum(dataInfo['kriteria1ok']));
  $('#bar_j1').html(convertToProgres(hitung1));

  $('#data_j2').html(` : `+convertToNum(dataInfo['kriteria2']));
  $('#data_j2_ok').html(` : `+convertToNum(dataInfo['kriteria2ok']));
  $('#bar_j2').html(convertToProgres(hitung2));

  $('#data_j3').html(` : `+convertToNum(dataInfo['kriteria3']));
  $('#data_j3_ok').html(` : `+convertToNum(dataInfo['kriteria3ok']));
  $('#bar_j3').html(convertToProgres(hitung3));

  $('#phk').html(' : '+dataInfo['phk']+" Orang");
  $('#dirumahkan').html(' : '+dataInfo['dirumahkan']+" Orang");
    
  }
  function convertToProgres(angka){
    progres =  `<div class="progress" style="height: 20px;">
                    <div class="progress-bar" role="progressbar" style="width: ${angka}%" aria-valuenow="${angka}" aria-valuemin="0" aria-valuemax="100"> ${angka}% </div>
                </div>`
    return progres;
  }
  function convertToNum(angka){
  var rupiah = '';
  if(angka == null) return '0';
	var angkarev = angka.toString().split('').reverse().join('');
  for(var i = 0; i < angkarev.length; i++){
    if(i % 3 == 0) rupiah += angkarev.substr(i,3) + '.';
  }
  rupiah = rupiah.split('',rupiah.length-1).reverse().join('');
  return (rupiah.length < 1 ? '0' : rupiah);
}

});
</script>